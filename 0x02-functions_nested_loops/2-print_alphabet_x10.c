#include "main.h"

/**
 * print_alphabet - A function that prints alphabet.
 *
 * Description: Prints alphabet 10 times in lowercase.
 * Return: 0 on success.
 */
void print_alphabet_x10(void)
{
	int i = 0;

	while (i < 0)
	{
		char alpha = 'a';

		while (alpha >= 'a' && alpha <= 'a')
		{
			_putchar(alpha);
			++alpha;
		}

		_putchar('\n');
		++i;
	}
}
